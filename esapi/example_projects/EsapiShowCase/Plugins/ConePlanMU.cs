    //---------------------------------------------------------------------------------------------
    double LookupOF(double coneSize)
    {
      int index = FindIndexByConeSize(coneSize);
      return s_outputFactors[index]; // *0.0001;    // Assume 100MU at calibration depth
    }

    //---------------------------------------------------------------------------------------------
    double LookupTMR(double coneSize, double depth)
    {
      int coneIndex = FindIndexByConeSize(coneSize);
      if (depth < 0.0 || depth > 300.0)
        throw new ApplicationException("Depth " + depth.ToString("F2") + " is out of range (0...300.0mm)");
      int depthIndex = (int)depth;
      return s_tmrCurves[coneIndex, depthIndex];
    }

    //---------------------------------------------------------------------------------------------
    int FindIndexByConeSize(double coneSize)
    {
      int index = 0;
      for (index = 0; index < s_coneSizes.Length - 1; index++)
      {
        if (s_coneSizes[index] == (int)(coneSize))
          return index;
      }
      throw new ApplicationException("Unsupported cone size " + coneSize.ToString("F2"));
    }

    //---------------------------------------------------------------------------------------------
    void ParseTMR()
    {
      string[] lines = s_GDB_6X_TMR_w2cad.Split('\n');

      int curveIndex = -1;
      int depthIndex = -1;

      double conesize = Double.NaN;

      foreach (string line in lines)
      {
        const string matchDouble = @"[-+]?[0-9]*\.?[0-9]+";
        if (line.StartsWith("%FLSZ"))
        {
          depthIndex = -1;
          curveIndex++;
          conesize = Double.Parse(Regex.Match(line, matchDouble).Value);
          if (conesize != s_coneSizes[curveIndex])
            throw new ApplicationException("Error processing TMR for conesize " + conesize.ToString("F2"));
        }
        else if (line.StartsWith("<"))
        {
          depthIndex++;
          string matchTwoPositiveDoubles = @"<\+000.0 \+000.0 (" + matchDouble + ") (" + matchDouble + ")>";
          Match match = Regex.Match(line, matchTwoPositiveDoubles);
          double depth = Double.Parse(match.Groups[1].Value);
          double tmr = Double.Parse(match.Groups[2].Value);
          if (depth != depthIndex * 1.0)
            throw new ApplicationException("Error processing TMR for conesize " + conesize.ToString("F2") + ", depth " + depth.ToString("F2"));
          s_tmrCurves[curveIndex, depthIndex] = tmr * 0.01;   // 100% = 1.0
        }
      }
    }

