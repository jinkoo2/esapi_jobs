    public void Execute(ScriptContext context, Window window)
    {
      window.Width = 400;
      window.Height = 600;

      window.Title = "Independent MU Recalculation for Cone Plan";

      PlanSetup plan = context.PlanSetup;
      if (plan == null || plan.StructureSet == null || plan.Dose == null)
      {
        MessageBox.Show("Please, open a plan with calculated 3D dose");
        return;
      }

      List<string> messages = new List<string>();
      IndependentMURecalculation(plan, messages);

      ListBox listbox = new System.Windows.Controls.ListBox();
      listbox.ItemsSource = messages;
      window.Content = listbox;
    }

    //---------------------------------------------------------------------------------------------
    private void IndependentMURecalculation(PlanSetup plan, List<string> messages)
    {

      if (plan.TreatmentOrientation != PatientOrientation.HeadFirstSupine ||
          plan.StructureSet.Image.ImagingOrientation != PatientOrientation.HeadFirstSupine)
      {
        messages.Add("Only HFS/HFS is supported!");
        return;
      }

      ParseTMR();

      VVector isocenter = new VVector(Double.NaN, Double.NaN, Double.NaN);
      plan.DoseValuePresentation = DoseValuePresentation.Relative;

      foreach (Beam beam in plan.Beams)
      {
        if (Double.IsNaN(isocenter.x))
        {
          isocenter = beam.IsocenterPosition;
        }
        else
        {
          if ((isocenter - beam.IsocenterPosition).Length > 0.001)
            throw new ApplicationException("Only single isocenter plans are supported");
        }

        if (beam.Applicator == null)
        {
          messages.Add(beam.Id + " no cone.");
          continue;
        }

        double averageSSD = AverageSSD(beam, plan.StructureSet.Image);
        double averageDepth = beam.TreatmentUnit.SourceAxisDistance - averageSSD;
        double coneSize = ParseConeSize(beam.Applicator);
        double TMR = LookupTMR(coneSize, averageDepth); 
        double OF = LookupOF(coneSize);

        DoseValue doseAtIso = plan.Dose.GetDoseToPoint(beam.IsocenterPosition);
        double weightAtIso  = plan.Beams.Sum(o => o.WeightFactor);
        double doseToWeightFactor = weightAtIso / doseAtIso.Dose;

        double prescribedFractionDose = plan.UniqueFractionation.PrescribedDosePerFraction.Dose;

        if (plan.UniqueFractionation.PrescribedDosePerFraction.Unit == DoseValue.DoseUnit.cGy)
          prescribedFractionDose *= 0.01;    // use Gy

        double repeatFactor = (prescribedFractionDose / (plan.PrescribedPercentage)) / doseToWeightFactor;
        
        double beamWeight = beam.WeightFactor;
        double MU = (repeatFactor * beamWeight / TMR) / OF;
        double eclipseMU = beam.Meterset.Value;
        double eclipseDepth = beam.TreatmentUnit.SourceAxisDistance - beam.AverageSSD;

        messages.Add(string.Empty);
        messages.Add("Beam Id: " + beam.Id);
        messages.Add("Depth Eclipse: " + eclipseDepth.ToString("F2"));
        messages.Add("Depth Recalc: " + averageDepth.ToString("F2"));
        messages.Add("Depth Diff: " + (eclipseDepth - averageDepth).ToString("F2"));
        messages.Add("Cone diameter(mm): " + coneSize.ToString("F0"));
        messages.Add("TMR: " + TMR.ToString("F3"));
        messages.Add("OF: " + OF.ToString("F3"));
        messages.Add("MU Eclipse: " + eclipseMU.ToString("F0"));
        messages.Add("MU Recalc: " + MU.ToString("F0"));
        messages.Add("MU Diff %: " + (((eclipseMU - MU) / MU) * 100).ToString("F1"));
      }
    }

    //---------------------------------------------------------------------------------------------
    private double ParseConeSize(Applicator applicator)
    {
      double diameter = Double.NaN;
      string id = applicator.Id;
      string number = Regex.Match(id, @"[0-9]+").Value;
      Double.TryParse(number, out diameter);
      return diameter;
    }

    //---------------------------------------------------------------------------------------------
    private double DegToRad(double deg) { return deg * Math.PI / 180.0; }

    //---------------------------------------------------------------------------------------------
    private Matrix3D GetRotationMatrix(double alpha, double beta, double gamma)
    {
      double sina = Math.Sin(alpha), cosa = Math.Cos(alpha);
      double sinb = Math.Sin(beta), cosb = Math.Cos(beta);
      double sing = Math.Sin(gamma), cosg = Math.Cos(gamma);

      Matrix3D mtx = new Matrix3D();
      mtx.M11 = cosb * cosg;
      mtx.M12 = cosb * sing;
      mtx.M13 = -sinb;
      mtx.M21 = sina * sinb * cosg - cosa * sing;
      mtx.M22 = sina * sinb * sing + cosa * cosg;
      mtx.M23 = sina * cosb;
      mtx.M31 = cosa * sinb * cosg + sina * sing;
      mtx.M32 = cosa * sinb * sing - sina * cosg;
      mtx.M33 = cosa * cosb;
      return mtx;
    }

    //---------------------------------------------------------------------------------------------
    private double AverageSSD(Beam beam, VMS.TPS.Common.Model.API.Image image)
    {
      if (beam.ControlPoints.Count != 2)
        throw new InvalidOperationException("Wrong number of control points");

      double start = beam.ControlPoints[0].GantryAngle;
      double stop = beam.ControlPoints[1].GantryAngle;
      if (start > stop)
      {
        start = beam.ControlPoints[1].GantryAngle;
        stop = beam.ControlPoints[0].GantryAngle;
      }
      double cumulativeSum = 0.0;
      int countInSum = 0;
      for (double gantry = start; gantry < stop; gantry += 1.0)
      {
        double couchRtn = beam.ControlPoints[0].PatientSupportAngle;
        Matrix3D mtxGantry = GetRotationMatrix(0, 0, DegToRad(gantry));
        Matrix3D mtxCouch = GetRotationMatrix(0, DegToRad(couchRtn), 0);
        Vector3D v1 = mtxGantry.Transform(new Vector3D(0, -beam.TreatmentUnit.SourceAxisDistance, 0));
        Vector3D v2 = mtxCouch.Transform(v1);
        VVector sourcePosVerified = beam.IsocenterPosition + new VVector(v2.X, v2.Y, v2.Z);

        VVector sourcePos = beam.GetSourceLocation(gantry);
        if ((sourcePos - sourcePosVerified).LengthSquared > 0.0000001)
          throw new ApplicationException("Source position verification failed!");

        double ssd = FindDistanceToIntersection(image, sourcePos, beam.IsocenterPosition);
        cumulativeSum += ssd;
        countInSum++;
      }
      return cumulativeSum / countInSum;
    }

    //---------------------------------------------------------------------------------------------
    double FindDistanceToIntersection(VMS.TPS.Common.Model.API.Image image, VVector sourcePosition, VVector isocenterPosition)
    {
      double[] preallocatedBuffer = new double[5000];
      ImageProfile profile = image.GetImageProfile(sourcePosition, isocenterPosition, preallocatedBuffer);

      const double tresholdHU = -400;
      VVector intersectionPosition = profile.First(o => o.Value > tresholdHU).Position;

      return (intersectionPosition - sourcePosition).Length;
    }

